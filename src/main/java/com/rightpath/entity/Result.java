package com.rightpath.entity;

import java.time.LocalDateTime;

import com.fasterxml.jackson.annotation.JsonIgnore;
import com.rightpath.enums.AssessmentType;
import com.rightpath.enums.ResultStatus;

import jakarta.persistence.Column;
import jakarta.persistence.Entity;
import jakarta.persistence.EnumType;
import jakarta.persistence.Enumerated;
import jakarta.persistence.FetchType;
import jakarta.persistence.GeneratedValue;
import jakarta.persistence.GenerationType;
import jakarta.persistence.Id;
import jakarta.persistence.JoinColumn;
import jakarta.persistence.Lob;
import jakarta.persistence.ManyToOne;
import jakarta.persistence.OneToOne;
import lombok.AllArgsConstructor;
import lombok.Data;
import lombok.EqualsAndHashCode;
import lombok.NoArgsConstructor;
import lombok.ToString;

@Entity
@Data
@AllArgsConstructor
@NoArgsConstructor
public class Result {
	@Id
	@GeneratedValue(strategy = GenerationType.IDENTITY) // Auto-generated by the database.
	private Long id; // for id

	@Column(name = "candidate_email")
	private String candidateEmail;

	@Enumerated(EnumType.STRING)
	private AssessmentType assessmentType;

	private LocalDateTime submittedAt;
	private Double score;

	@Enumerated(EnumType.STRING)
	private ResultStatus status;

	@Lob // Use @Lob for larger data storage (for Hibernate compatibility)
	@Column(columnDefinition = "TEXT") // For JSON storage in databases like MySQL, H2
	private String resultsJson; // Stores the JSON result as a string

	// One-to-One relationship with Assessment
	@OneToOne
	@JoinColumn(name = "assessment_id")
	@JsonIgnore
	private Assessment assessment; // Associated assessment
	
	@Column(name = "job_prefix")
	private String jobPrefix;

	@ManyToOne(fetch = FetchType.LAZY)
	@JoinColumn(name = "candidate_email", referencedColumnName = "email", insertable = false, updatable = false)
	@ToString.Exclude
	@EqualsAndHashCode.Exclude
	@JsonIgnore
	private Users candidate;

	@ManyToOne(fetch = FetchType.LAZY)
	@JoinColumn(name = "job_prefix", referencedColumnName = "job_prefix", insertable = false, updatable = false)
	@ToString.Exclude
	@EqualsAndHashCode.Exclude
	@JsonIgnore
	private JobPost jobPost;

}
